package org.example;

public class JavaOperatorsDemo {
    public static void main(String[] args) {

        int a = 10123;
        int b = 20;

        System.out.println(a);
        a++; // increment by 1
        System.out.println(a);
        a--; // decrement by 1
        System.out.println(a);

        System.out.println(a/b); // divides, but excludes number after .
        System.out.println(a%b); // returns the remainder
    }
}
