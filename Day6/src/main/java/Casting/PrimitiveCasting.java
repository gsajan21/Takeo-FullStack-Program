package Casting;

public class PrimitiveCasting {


    public static void main(String[] args) {

        int number = 100;
        System.out.println(number);
        byte b = (byte) number;
        System.out.println(b);

        double d = 2564.354;
        System.out.println(d);
        float f = (float) d;
        System.out.println(f);
    }
}
